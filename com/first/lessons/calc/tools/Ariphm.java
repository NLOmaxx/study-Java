package com.lessons.calc.tools;

public class Ariphm {
    public static int sum(int a, int b) {
        int res = a+b;
        return res;
    }

    public static int minus(int a, int b) {
        int res = a-b;
        return res;
    }

    public static int mult(int a, int b) {
        int res = a*b;
        return res;
    }

    public static int div(int a, int b) {
        int res = a/b;
        return res;
    }
}
